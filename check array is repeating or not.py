# WAP to tell whether an array contains distinct elements or not.//not repeating element 
lst=[4,3,5,6,3,8,9,4,4,4,4]
for i in range(0,10):
    for j in range(i+1,11):
        if lst[i]==lst[j]:
            print("list has repeating element",lst[i])
         

    

    
    
     
    
    
    
    
    
    
    
    
     